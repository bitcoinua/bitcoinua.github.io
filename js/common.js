// Add Load Handler v1.0
// Adds an onload handler for script and iframe elements (supports IE)
// Dependencies: Queue Callback, Add Event Handler, Generate Numeric ID
fxcmsc._addLoadHandlerCallbackFired = {};
fxcmsc._addLoadHandler = function(element, callback) {
	if (typeof callback !== 'function') { return false; }

	var callbackID = fxcmsc._generateNumericID();	// Generate an ID for the callback
	fxcmsc._addLoadHandlerCallbackFired[callbackID] = false;	// Initialize its state as not fired
	callback = fxcmsc._queueCallback(callback, 'fxcmsc._addLoadHandler:' + callbackID);	// Support multiple callbacks on the same element
	var wrappedCallback = function() {	// Wrap callback to set state to fired when called
			fxcmsc._addLoadHandlerCallbackFired[callbackID] = true;
			return callback.call(element);
		};

	// Attach standard load handler
	fxcmsc._addEventHandler(element, 'load', wrappedCallback);

	/* Hack to replicate element.onload in IE
	Adapted from Nick Spacek's code at https://gist.github.com/461797 */
	fxcmsc._addEventHandler(element, 'readystatechange', function() {
			if ((element.readyState === 'loaded' || element.readyState === 'complete') && fxcmsc._addLoadHandlerCallbackFired[callbackID] === false) {
				return wrappedCallback.call(element);
			}
		});

	return true;
};


// Queue Callback
// Queues callback functions to be executed in FIFO order
fxcmsc._callbacksQueues = {};
fxcmsc._queueCallback = function(callback, id) {
	if (typeof id === 'undefined') { id = callback; }

	if (typeof fxcmsc._callbacksQueues[id] === 'undefined') { fxcmsc._callbacksQueues[id] = []; }
	fxcmsc._callbacksQueues[id].push(callback);

	return function() {
		while (fxcmsc._callbacksQueues[id].length > 0) {
			fxcmsc._callbacksQueues[id].shift().apply(this, arguments);
		}
	};
};


