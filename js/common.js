// Is Cross Domain v1.0 - Identifies cross-domain links eligible for tracking parameters
// Dependencies: GetRootDomain
// Settings: s.internalDomains, s.blockedInternalDomains
fxcmsc._isCrossDomain = function(URL) {
	var externalDomain = fxcmsc._getRootDomain(URL);

	// Confirm that a root domain was found in URL
	if (!externalDomain) { return false; }

	// Parse the ignored file types into an array
	var CrossSiteIgnoredFileTypesArray = s.CrossSiteIgnoredFileTypes.toLowerCase().split(',');

	// Look for a match in the internal domains list
	for (var domainIndex = 0; domainIndex <= s.internalDomains.length; domainIndex++) {
		if (s.internalDomains[domainIndex] === externalDomain || externalDomain.indexOf('.' + s.internalDomains[domainIndex]) > -1) {
			// Check for blacklisted subdomains of whitelisted domains
			for (var blacklistDomainIndex = 0; blacklistDomainIndex < s.blockedInternalDomains.length; blacklistDomainIndex++) {
				if (s.blockedInternalDomains[blacklistDomainIndex] === externalDomain) {
					return false;
				}
			}

			// Check for ignored file types
			if (URL.length > 4) {
				var extension = URL.toLowerCase().slice(URL.length - 4, URL.length);
				if (extension.charAt(0) === '.') {
					for (var ExtensionIndex = 0; ExtensionIndex < CrossSiteIgnoredFileTypesArray.length; ExtensionIndex++) {
						if (extension === '.' + CrossSiteIgnoredFileTypesArray[ExtensionIndex]) {
							return false;
						}
					}
				}
			}

			return true;
		}
	}

	// If no internal domains match, it is not a cross domain URL
	return false;
};


